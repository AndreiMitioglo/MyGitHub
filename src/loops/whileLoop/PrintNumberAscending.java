package loops.whileLoop;

public class PrintNumberAscending {
    public static void main(String[] args) {

    // i loop, for loop
        for (int i = 1; i <= 7; i++) // print numbers from 1 to 7 (both included)
            System.out.println(i);


    // while loop
        int i = 1; // starting point
        while(i <=7){ // end point
            System.out.println(i);
            i++;  // adding
        }





    }
}
