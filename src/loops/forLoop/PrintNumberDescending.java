package loops.forLoop;

public class PrintNumberDescending {

    public static void main(String[] args) {
        /*
Decrement from 100 to 0 and print it.

         */
      // start point 100; end point 0;   decrement
        for (int i = 100; i >= 0; i--) { // decrement by -1;
            System.out.println(i); // 100.. 99..98..97

        }
    }
}