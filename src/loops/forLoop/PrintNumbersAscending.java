package loops.forLoop;

public class PrintNumbersAscending {
    public static void main(String[] args) {
        /*
        Write a Java program to print numbers 1 to 10;

        start point = 1;  end point<=10;  post-increment
         */


       //start point = 1;  end point<=10;  post-increment
        for (int num = 1;  num <=100;      num += 10){
            System.out.println(num);
        }

    }
}
